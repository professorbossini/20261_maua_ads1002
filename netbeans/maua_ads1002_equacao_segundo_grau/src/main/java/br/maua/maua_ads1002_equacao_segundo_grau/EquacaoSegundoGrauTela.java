    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        //pegar o que o usuário digitou
        //float(input())
        double a = Double.parseDouble(txtA.getText());
        double b = Double.parseDouble(txtB.getText());
        double c = Double.parseDouble(txtC.getText());
        if(a == 0){
            //exibir o texto "Não é equação de segundo grau"
            txtResultado.setText("Não é equação de segundo grau");
        }
        else{
            //fazer as continhas
            double delta = Math.pow(b, 2) - 4 * a * c;
            if(delta < 0){
                txtResultado.setText("Sem raizes");
            }
            else if(delta == 0){
                double x = -b / (2 * a);
                txtResultado.setText(String.format(
                    "Uma raiz: %.2f", x
                ));
            }
            else{
                double x1 = (-b + Math.sqrt(delta)) / (2 * a);
                double x2 = (-b - Math.sqrt(delta)) / (2 * a);
                txtResultado.setText(String.format(
                    "Duas raizes: %.2f e %.2f", x1, x2
                ));
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

